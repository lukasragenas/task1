    static boolean gameOver;
    static boolean quit = false;

    public static class Bird {
        static int birdX;
        static int birdY;
    }

    public static void main(String[] args) throws IOException {

        GameMap gameMap = new GameMap();
        Bird bird = new Bird();
        printMenu();
        while (!quit) {
            int score = 0;
            int action = readUserInput();
            switch (action) {
                case 'p':
                    bird.birdX = 10;
                    bird.birdY = 8;
                    gameOver = false;
                    int copyArray[][] = new int[gameMap.MAP.length][gameMap.MAP[0].length];
                    createNewMap(gameMap, copyArray);
                    while (!gameOver) {
                        printMap(bird, copyArray);
                        int key = readUserInput();
                        moveBird(key, bird, copyArray);
                        shiftArray(copyArray, 1);
                        clearConsole();
                        gameInfo();
                        score = countScore(score, bird, copyArray);
                    }
                    clearConsole();
                    printMenu();
                    break;
                case 'q':
                    printQuitMessage();
                    quit = true;
                    break;
            }
        }
    }

    private static void createNewMap(GameMap gameMap, int[][] copyArray) {
        for (int i = 0; i < gameMap.MAP.length; i++) {
            for (int j = 0; j < gameMap.MAP[0].length; j++) {
                copyArray[i][j] = gameMap.MAP[i][j];
            }
        }
    }

    private static void gameInfo() {
        System.out.println("Press e to exit");
    }

    public static void shiftArray(int[][] array, int shift) {

        for (int row = 0; row < array.length; row++) {
            int rowLength = array[row].length;

            shift = shift % rowLength;

            int[] tmp = new int[shift];
            for (int i = 0; i < shift; i++) {
                tmp[i] = array[row][i];
            }

            for (int col = 0; col < rowLength - shift; col++) {
                array[row][col] = array[row][col + shift];
            }

            for (int i = 0; i < shift; i++) {
                array[row][i + (rowLength - shift)] = tmp[i];
            }
        }
    }


    private static void printQuitMessage() {
        System.out.println("Thanks for playing.");
    }

    private static int countScore(int score, Bird bird, int[][] gameMap) {
        if (gameMap[1][bird.birdX - 1] == 1)
            score++;
        System.out.println("Score: " + (score / 2) / 4);
        return score;
    }

    private static void clearConsole() {
        for (int i = 0; i < 100; i++)
            System.out.println("\b");
    }

    private static void printMenu() {
        System.out.println("MAIN MENU");
        System.out.println("=========");
        System.out.println("Press 'p' to play");
        System.out.println("Press 'q' to quit");
    }

    private static void moveBird(int key, Bird bird, int[][] gameMap) {
        switch (key) {
            case 'w':
                if (gameMap[bird.birdY - 1][bird.birdX + 1] != 1 && gameMap[bird.birdY - 1][bird.birdX + 1] != 2) {
                    bird.birdY--;
                } else {
                    System.out.println("Game Over");
                    gameOver = true;
                }
                break;
            case 's':
                if (gameMap[bird.birdY + 1][bird.birdX + 1] != 1 && gameMap[bird.birdY + 1][bird.birdX + 1] != 2) {
                    bird.birdY++;
                } else {
                    System.out.println("Game Over");
                    gameOver = true;
                }
                break;
            case 'e':
                gameOver = true;
                break;
        }
    }

    private static int readUserInput() throws IOException {
        return System.in.read();
    }

    private static void printMap(Bird bird, int[][] gameMap) {
        for (int i = 0; i < gameMap[0].length; i++) {
            System.out.print('=');
        }
        for (int y = 0; y < gameMap.length; y++) {
            for (int x = 0; x < gameMap[0].length; x++) {
                if (y == bird.birdY && x == bird.birdX) {
                    System.out.print('B');
                } else if (gameMap[y][x] == 0)
                    System.out.print(' ');
                else if (gameMap[y][x] == 1)
                    System.out.print('#');
            }
            System.out.println();
        }
        for (int i = 0; i < gameMap[0].length; i++) {
            System.out.print('=');
        }
        System.out.println();
    }
